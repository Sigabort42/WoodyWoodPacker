#include "libft.h"

char	*ft_strdup(char *s1)
{
	int		i;
	char	*str;

	i = 0;
	if ((str = (char *)malloc(sizeof(*str) * ft_strlen(s1) + 1)) == NULL)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
